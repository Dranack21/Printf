#include "ft_printf.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		while (n >= 10)
		{
			ft_putnbr(n / 10);
			n = n % 10;
		}
		ft_putchar(n + 48);
	}
}